package com.entitylayer;

public class Taxdetailsupdate {
	private String status;
	private String type;
	private long agi;
	private long itemized;
	private long standardised;
	private String phno;
	private String year;

	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public long getAgi() {
		return agi;
	}
	public void setAgi(long agi) {
		this.agi = agi;
	}
	public long getItemized() {
		return itemized;
	}
	public void setItemized(long itemized) {
		this.itemized = itemized;
	}
	public long getStandardised() {
		return standardised;
	}
	public void setStandardised(long standardised) {
		this.standardised = standardised;
	}
	public String getPhno() {
		return phno;
	}
	public void setPhno(String phno) {
		this.phno = phno;
	}
}
